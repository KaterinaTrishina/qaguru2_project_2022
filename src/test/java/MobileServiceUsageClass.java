public class MobileServiceUsageClass {
    private double incomingMobileVoiceUsage;
    private double outgoingMobileVoiceUsage;
    private double internetBucketUsage;
    private int outgoingSmsAmount;
    private int incomingSmsAmount;


    public double getIncomingMobileVoiceUsage() {
        return incomingMobileVoiceUsage;
    }

    public void setIncomingMobileVoiceUsage(double incomingMobileVoiceUsage) {
        this.incomingMobileVoiceUsage = incomingMobileVoiceUsage;
    }

    public double getOutgoingMobileVoiceUsage() {
        return outgoingMobileVoiceUsage;
    }

    public void setOutgoingMobileVoiceUsage(double outgoingMobileVoiceUsage) {
        this.outgoingMobileVoiceUsage = outgoingMobileVoiceUsage;
    }

    public double getInternetBucketUsage() {
        return internetBucketUsage;
    }

    public void setInternetBucketUsage(double internetBucketUsage) {
        this.internetBucketUsage = internetBucketUsage;
    }

    public int getOutgoingSmsAmount() {
        return outgoingSmsAmount;
    }

    public void setOutgoingSmsAmount(int outgoingSmsAmount) {
        this.outgoingSmsAmount = outgoingSmsAmount;
    }

    public int getIncomingSmsAmount() {
        return incomingSmsAmount;
    }

    public void setIncomingSmsAmount(int incomingSmsAmount) {
        this.incomingSmsAmount = incomingSmsAmount;
    }
}
